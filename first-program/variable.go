package main

import "fmt"

func main() {
	// Numeric variables
	var num1 int = 42       // Integer
	var num2 float64 = 3.14 // Float

	// Boolean variable
	var isGoFun bool = true // Boolean

	// String variable
	var message string = "Hello, Go!"

	// Print variables
	fmt.Println("Integer value:", num1)
	fmt.Println("Float value:", num2)
	fmt.Println("Boolean value:", isGoFun)
	fmt.Println("String value:", message)
}
